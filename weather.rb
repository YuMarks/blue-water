weather = 'foggy'
case weather
when 'foggy'
	puts "Wear miner's cap"
when 'sunny'
	puts "Wear sandals and big floppy hat"
when 'rainy'
	puts "Might as well throw you hair into a ponytail and get umbrella"
when 'windy'
	puts "Put on a jacket and let the wind style your hair"
when 'snowing'
	puts "Wear heavy boots, gloves a hat and a scarf and a big, puffy coat."
else
	puts "Hmm, may you should just stay home"
end